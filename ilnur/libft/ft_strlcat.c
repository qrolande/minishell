#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (size == 0)
		return (ft_strlen(src));
	while (dst[i] != '\0' && i < size)
		i++;
	while (src[j] != '\0' && (i + j) < (size - 1))
	{
		dst[j + i] = src[j];
		j++;
	}
	if (i < size)
		dst[j + i] = '\0';
	return (i + ft_strlen(src));
}
