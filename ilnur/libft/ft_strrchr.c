#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (str[i] == c)
			return (&*(char *)(str + i));
		i--;
	}
	if (c == '\0')
		return ((char *)str);
	return (NULL);
}
